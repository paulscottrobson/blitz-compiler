;										Compile a Data Block
;
; ************************************************************************************************

CommandDATA:
		jsr 	BufferClear 				; copy it to the buffer
		jsr 	LookNextNonSpace
_CTDataLoop:
		jsr 	LookNext 					; reached EOL
		beq 	_CTDataDone
		;
		jsr 	BufferWrite 				; write and consume
		jsr 	GetNext
		bra 	_CTDataLoop
_CTDataDone:
		lda 	#PCD_CMD_DATA 				; output command and buffer
		jsr 	WriteCodeByte
		jsr 	BufferOutput
		rts		

		.send code


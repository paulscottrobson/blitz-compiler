CommandWAIT: 
		jsr 	LookNextNonSpace
		cmp 	#","
		beq 	_CWThirdParameter
		lda 	#0
		jsr 	PushIntegerA
_CWExit:		
		.keyword PCD_WAIT
		rts

_CWThirdParameter:
		jsr 	GetNext
		jsr 	CompileExpressionAtA
		and 	#NSSTypeMask
		cmp 	#NSSIFloat
		beq 	_CWExit
		.error_type

		.send code



; ************************************************************************************************
;
;											CMD command
;
; ************************************************************************************************

CommandCMD:
		lda 	#PCD_SETCHANNEL 			; set the channel to this now forever.
		jsr 	WriteCodeByte
		jsr 	LookNextNonSpace 			; followed by a , ?
		cmp 	#","
		bne 	_CCMDExit
		jsr 	GetNext 					; consume comma.
		jsr 	CommandPRINT 				; do the print code
_CCMDExit:
		rts		
		.send code


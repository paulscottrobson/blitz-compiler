		.section code

; ************************************************************************************************
;
;								Set error handler to YX
;
; ************************************************************************************************

SetErrorHandler:
		sty 	ErrorHandlerVector+1
		stx 	ErrorHandlerVector
		rts
		
; ************************************************************************************************
;
;								Go to current error handler
;
; ************************************************************************************************

CallErrorHandler:
		jmp 	(ErrorHandlerVector)

		.send code

		.section storage
ErrorHandlerVector:
		.fill 	2
		.send storage


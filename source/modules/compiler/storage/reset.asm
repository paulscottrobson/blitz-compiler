		.section code

; ************************************************************************************************
;
;								Reset the storage (variables,line#)
;
; ************************************************************************************************

STRReset:
		.set16 	lineNumberTable,WorkArea+WorkAreaSize
		.set16  variableListEnd,WorkArea
		.storage_access
		stz 	variableListEnd
		.storage_release
		rts

		.send code


		.section storage
lineNumberTable:							; line number table, works down.
		.fill 	2		
variableListEnd:							; known variables, works up.
		.fill 	2		
		.send storage


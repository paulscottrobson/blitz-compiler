; ************************************************************************************************
;
;						Get Input from Channel if available, else return 0
;										0 is the Keyboard
;
; ************************************************************************************************

XGetCharacterFromChannel:
		phx
		phy

		jsr 	$FFC6 						; CHKIN set channel (0 = keyboard)
		jsr 	$FFB7 						; check okay
		bne 	_XGCError
		jsr 	$FFE4
		ply
		plx
		rts
_XGCError:
		.error_channel

		.send code


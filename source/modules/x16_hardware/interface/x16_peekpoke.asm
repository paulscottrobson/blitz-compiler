XPokeMemory:
		stx 	zTemp0
		sty 	zTemp0+1
		sta 	(zTemp0)
		rts

; ************************************************************************************************
;
;											Read Memory
;
; ************************************************************************************************

XPeekMemory:
		stx 	zTemp0
		sty 	zTemp0+1
		lda 	(zTemp0)
		rts


;								BIN$ (integer to string)
;
; ************************************************************************************************

UnaryBin: ;; [bin$]
		.entercmd
		jsr 	GetInteger16Bit				; 16 bit int 
		lda 	#16 						; allocate / set 16 bytes.
		jsr 	StringAllocTemp
		lda 	zTemp0+1
		beq 	_UBNoHigh
		jsr 	_UBWriteBinary
_UBNoHigh:
		lda 	zTemp0
		jsr 	_UBWriteBinary
		.exitcmd

_UBWriteBinary:		
		ldy 	#8
_UBWLoop:
		asl 	a
		pha
		lda  	#0
		adc 	#48		
		jsr 	StringWriteChar
		pla
		dey
		bne 	_UBWLoop
		rts


		.send code


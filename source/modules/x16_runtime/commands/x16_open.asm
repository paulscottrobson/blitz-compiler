CommandOpen: ;; [open]
		.entercmd
		;
		;		Set up the file name
		;
		lda 	NSMantissa0+3  				; point zTemp0 to string head, also in XY
		sta 	zTemp0
		tay
		lda 	NSMantissa1+3 
		sta 	zTemp0+1
		tax

		iny 								; XY points to first character
		bne 	_CONoCarry
		inx
_CONoCarry:		
		lda 	(zTemp0) 					; get length of filename
		jsr 	$FFBD
		.debug
		.exitcmd


		.send 	code
		

PrintSpace: ;; [print.spc]
		.entercmd
		lda 	NSMantissa0,x 	
		dex
_PSLoop:	
		cmp 	#0
		beq 	_PSExit
		pha
		lda 	#" "
		jsr 	XPrintCharacter
		pla
		dec 	a
		bra 	_PSLoop
_PSExit:
		.exitcmd
		

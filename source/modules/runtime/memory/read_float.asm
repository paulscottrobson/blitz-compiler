;
;								Write float (2 byte command)
;
; ************************************************************************************************

ReadFloatCommand:
		.entercmd
		.vaddress

ReadFloatZTemp0:
		phy 								; start write
		ldy 	#1
		inx

		lda 	(zTemp0)
		sta 	NSMantissa0,x
		
		lda 	(zTemp0),y
		sta 	NSMantissa1,x
		iny

		lda 	(zTemp0),y
		sta 	NSMantissa2,x
		iny

		lda 	(zTemp0),y
		sta 	NSMantissa3,x
		iny

		lda 	(zTemp0),y
		sta 	NSExponent,x
		iny

		lda 	(zTemp0),y
		sta 	NSStatus,x

		ply
		.exitcmd
		.send 	code
		

		.section 	code

; ************************************************************************************************
;
;								Write string (2 byte command)
;
; ************************************************************************************************

WriteStringCommand:
		.entercmd
		.vaddress

WriteStringZTemp0:
		phy

		ldy 	NSMantissa1,x 				; get string value
		lda 	NSMantissa0,x
		jsr 	StringConcrete 				; concrete it.

		sta 	(zTemp0) 					; write out LSB

		tya  								; write out MSB
		ldy 	#1
		sta 	(zTemp0),y

		dex 								; pop string off stack.
		ply
		.exitcmd
		.send 	code
		

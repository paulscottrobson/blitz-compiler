		.section 	code

; ************************************************************************************************
;
;					Command which receives limit of variable memory
;
; ************************************************************************************************

CommandVarSpace: ;; [.varspace]
		.entercmd
		  
		lda 	(codePtr),y					; 3 byte opcode, which is 'free' memory in variable area.
		sta 	availableMemory
		iny 
		lda 	(codePtr),y
		sta 	availableMemory+1
		iny

		.exitcmd
		.send 	code

		.section storage
availableMemory: 							; available memory as offset
		.fill 	2
		.send storage				

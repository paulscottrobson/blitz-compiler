		.section 	code

; ************************************************************************************************
;
;									     Time to TOS
;
; ************************************************************************************************

TimeTOS:	;; [ti]
		.entercmd
		jsr 	TIPushClock 				; push clock to TOS
		.exitcmd

TimeString: ;; [ti$]
		.entercmd
		jsr 	TIPushClock 				; push clock to TOS

		.debug


		.exitcmd

; ************************************************************************************************
;
;									Push clock (60Hz) to TOS
;	
; ************************************************************************************************

TIPushClock:
		phy
		inx 								; push 0 on the stack
		jsr 	FloatSetZero
		phx
		jsr 	XReadClock 					; read time into AXY
		stx 	zTemp0
		plx
		sta 	NSMantissa0,x
		lda 	zTemp0
		sta 	NSMantissa1,x
		tya
		sta 	NSMantissa2,x
		ply
		rts
		.send 	code


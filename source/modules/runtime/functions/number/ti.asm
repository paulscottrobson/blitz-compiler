TimeTOS:	;; [ti]
		.entercmd
		phy
		inx 								; push 0 on the stack
		jsr 	FloatSetZero
		phx
		jsr 	XReadClock 					; read time into AXY
		stx 	zTemp0
		plx
		sta 	NSMantissa0,x
		lda 	zTemp0
		sta 	NSMantissa1,x
		tya
		sta 	NSMantissa2,x
		ply
		.exitcmd

		.send 	code


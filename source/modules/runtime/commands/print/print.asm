		.section code

; ************************************************************************************************
;
;									Set Print Channel, return old in A
;
; ************************************************************************************************

SetPrintChannel:
		phx
		ldx 	currentChannel
		sta 	currentChannel
		txa
		plx
		rts

; ************************************************************************************************
;
;						  				Print Character
;
; ************************************************************************************************

VectorPrintCharacter:
		phx
		ldx 	currentChannel
		jsr 	XPrintCharacterToChannel
		plx
		rts

; ************************************************************************************************
;
;						  				Get Character
;
; ************************************************************************************************

VectorGetCharacter:
		phx
		ldx 	currentChannel
		jsr 	XGetCharacterFromChannel
		plx
		rts

		.send code

		.section storage
currentChannel:
		.fill 	1
		.send storage


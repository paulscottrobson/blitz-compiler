UnaryTangent: 	

		txa 								; sin -> +1
		tay
		iny
		jsr 	CopyFloatXY
		inx
		jsr 	CalculateSine 				
		dex

		txa 								; cos -> +2
		tay
		iny
		iny
		jsr 	CopyFloatXY
		inx
		inx
		jsr 	CalculateCosine 				

		dex  								; divide sin/cos
		jsr 	FloatDivide

		txa 								; copy result down.
		tay
		dey
		jsr 	CopyFloatXY 				
		dex

		clc
		rts		


		.send 	code


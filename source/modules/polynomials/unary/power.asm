BinaryPower: 
		dex

		txa 							; copy 0 to 2, so we can process it
		tay
		iny
		iny
		jsr 	CopyFloatXY


		inx 							; 2 = Log(0)
		inx

		jsr 	LogCode

		dex 							; Multiply by original 1, into 1.
		jsr 	FloatMultiply

		txa 							; copy to slot 0
		tay
		dey
		jsr 	CopyFloatXY

		dex  							; Exponent code.

		jsr 	ExpCode 				
		clc
		rts
		
		.send 	code


CommandShift: ;; [.shift]
		lda 	(codePtr),y 				; get shifted command (X already pushed)
		iny
		asl 	a 							; double into X
		tax 	
		jmp 	(ShiftVectorTable,x) 		; go there.

		.send 	code
		

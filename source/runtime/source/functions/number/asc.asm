;								  ASCII value of first character.
;
; ************************************************************************************************

		.section code

UnaryAsc: ;; [asc]
		.entercmd
		phy
		;
		lda 	NSMantissa0,x 				; string address.
		sta 	zTemp0
		lda 	NSMantissa1,x
		sta 	zTemp0+1
		;
		lda 	(zTemp0) 					; if empty string return zero
		beq 	_UAExit
		phy 								; otherwise first character
		ldy 	#1
		lda 	(zTemp0),y
		ply
_UAExit:
		jsr 	FloatSetByte
		ply
		.exitcmd

		.send code


UnaryFre:	;; [fre]
		.entercmd

		jsr 	FloatSetZero 				; zero the result (32 bit integer)
		sec
		lda 	stringHighMemory 			; calculate the free memory.
		sbc 	stringLowMemory
		sta		NSMantissa0,x
		lda 	stringHighMemory+1
		sbc 	stringLowMemory+1
		sta		NSMantissa1,x
		
		.exitcmd

		.send 	code


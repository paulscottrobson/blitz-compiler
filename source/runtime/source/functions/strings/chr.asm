		.section code

UnaryChr: ;; [chr$]
		.entercmd
		jsr 	GetInteger8Bit 				; get integer to convert.
		pha 								; save it and allocate for it
		lda 	#1 							; 1 character
		jsr 	StringAllocTemp 	
		lda 	#1 							; length 1.
		sta 	(zsTemp)
		pla 								; character code makes string.
		phy
		ldy 	#1
		sta 	(zsTemp),y
		ply
		.exitcmd

		.send code


CommandExit: ;; [exit]
		.entercmd
		stx 	zTemp0 						; stack position.
		jmp 	$FFFF 						; exits the emulator.

CommandDebug: ;; [debug]
		.entercmd
		.debug
		.exitcmd

		.send 	code
		

;								GET char to stack as string
;
; ************************************************************************************************

		.section code

CommandGet: ;; [get]
		.entercmd
		inx
		lda 	#1 							; 1 character space
		jsr 	StringAllocTemp 	

		jsr 	VectorGetCharacter 			; get a character
		cmp 	#0
		beq 	_CGNone

		phy
		ldy 	#1 							; store char
		sta 	(zsTemp),y
		tya 								; store length.
		sta 	(zsTemp)
		ply
_CGNone:		
		.exitcmd

		.send code


CommandAssert: ;; [assert]
		.entercmd
		cpx 	#0 							; check only one element on stack
		bne 	_CAFail
		jsr 	FloatIsZero 				; is it zero ?
		beq 	_CAFail
		dex	
		.exitcmd

_CAFail:
		.error_assert		

		.send 	code
		

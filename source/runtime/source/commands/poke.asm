CommandPOKE: ;; [poke]
		.entercmd
		phx 								; save XY
		phy
		jsr 	GetInteger8Bit 				; get byte to POKE and save it
		pha 								
		lda 	NSMantissa1-1,x 			; YX = address from Mantissa next up
		tay
		lda 	NSMantissa0-1,x
		tax
		pla
		jsr 	XPokeMemory
		ply 								; restore YX and drop 2
		plx
		dex
		dex
		.exitcmd


